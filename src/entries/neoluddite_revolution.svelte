<h1>Neo-Luddite Revolution: Some thoughts on slowing down the churn</h1>
<h3>part i of ii</h3>
<h2>27 May 2025</h2>

<h3>who were the luddites and why should i care?</h3>
<p>
	The Luddites were a group of English textile craftsmen. In the early 19th century when the
	Luddites were around, there was a dramatic advancement in the technology used to manufacture
	cloth. This development decreased their leverage on the labor market, since less labor was needed
	to produce a similar quantity of cloth using the new machines. It also decreased the average
	quality of textiles being produced, since the fabric formerly produced by skilled craftspeople was
	superior to that manufactured with the new machines. In response to the resultant degradation of
	their working and living conditions, they engaged in a lot of the usual tactics of organized
	labor, including (as you probably already know!) strategic acts of industrial sabotage.
</p>
<p>
	Let's state the obvious: the Luddites lost. In concrete terms, the Luddites were defeated because
	factory owners were well-organized and had the power of the state behind them. Mill owners would
	(extralegally) shoot members of Luddite actions and organizations, and eventually the English
	state backed this up through executions and deportation to its penal colonies. In historical
	terms, the successful industrialization of cloth production jump-started the capitalist world
	system we universally-though-unevenly live in today. And in the shorter term, the higher profit
	margins briefly afforded by this advance, in combination with the cotton gin's industrialization
	of the cotton harvest, led to a brutal intensification of chattel slavery in the US in the years
	immediately preceding its (formal) abolition, degrading working conditions worldwide by lowering
	the floor for the treatment of laborers.<SuperScript id="dubois" />
</p>
<p>
	But it is a truism to state that any society is structured in part by the failed revolutions of
	its predecessors.
</p>
<p>
	There are some obvious analogies between the situation of the Luddites and the situation which
	white collar workers in the developed world find themselves in today. I'll leave it to the reader
	to fill in the blanks. While the Luddites did not succeed in stopping industralization wholesale,
	they succeeded in slowing it down. Considering that the monetization of time is the primary engine <i
		>driving</i
	>
	further advancements, and considering that individual humans live for a set amount of time, it's clear
	that slowing down processes can be a worthy end even if it is not the <i>only</i> worthy end. If a
	given technological 'advancement' is not actually going to revolutionize the world --- say,
	forcing people to shop at VR Walmart --- slowing these processes down can also prevent the
	destruction of existing social structures without any viable plan for their rebuilding. I may not
	be able to stop LLMs from changing the way the industry writes code in the long run, but I can
	still (laugh in mild outrage and) push back when a half-baked Copilot extension <RainbowLink
		href="https://github.com/dotnet/runtime/pull/115762"
		>tries to inside job the dotnet runtime.</RainbowLink
	>
</p>
<h3>slow down, you say?</h3>
<p>
	Frankly, I don't have the remotest idea how to slow the crisis in reproduction we seem to be
	facing as a species. The Luddites had a far grander vision than I in this regard. I've been
	targeting much smaller problems. For instance: I hate how I interact with my phone and the
	internet more broadly. And yet my phone and the internet remain necessary and primary ways of
	engaging with the world at large. I've been experimenting with building up structures of attention
	that I
	<i>am</i> happy about participating in, to replace the ones I think are both personally
	unfulfilling and politically destructive. Going live with this post is <RainbowLink
		target=""
		href="/blog/intentional_attention">a second piece</RainbowLink
	> about some of the cool things I've been tinkering with in the interests of decoupling my emotional
	state from various automatic processes and re-coupling it more intentionally to other inputs. It's
	no general strike, but we all gotta start somewhere.
</p>
